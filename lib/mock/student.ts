export const mockStudents: Student[] = USE_DEMO_DATA ? DEMO_STUDENTS : [];

export function computeStudentStats(students: Student[]): StudentStatsData {
  return {
    totalStudents: students.length,
    activeStudents: students.filter((s) => s.status === "active").length,
    averageAttendance:
      students.length === 0
        ? 0
        : Math.round(
            students.reduce((sum, s) => sum + s.attendancePercentage, 0) /
              students.length
          ),
    pendingFeesAmount: students.reduce((sum, s) => sum + s.pendingFees, 0),
    pendingFeesCount: students.filter((s) => s.pendingFees > 0).length,
  };
}

export const mockStudentStats: StudentStatsData =
  computeStudentStats(mockStudents);

export function getStudentById(id: string): Student | undefined {
  return mockStudents.find((student) => student.id === id);
}

export function createStudent(data: StudentFormData): Student {
  const batch = getBatchById(data.batchId);

  const student: Student = {
    id: `stu-${Date.now()}`,
    firstName: data.firstName,
    lastName: data.lastName,
    fullName: `${data.firstName} ${data.lastName}`.trim(),
    email: data.email,
    phone: data.phone,
    gender: data.gender,
    dateOfBirth: data.dateOfBirth,
    guardianName: data.guardianName,
    guardianPhone: data.guardianPhone,
    address: data.address,
    batchId: data.batchId,
    batchName: batch?.name ?? "",
    school: data.school,
    status: data.status,
    attendancePercentage: 0,
    pendingFees: 0,
    createdAt: new Date().toISOString(),
  };

  mockStudents.unshift(student);
  return student;
}

export function updateStudent(
  id: string,
  data: StudentFormData
): Student | null {
  const index = mockStudents.findIndex((student) => student.id === id);
  if (index === -1) return null;

  const batch = getBatchById(data.batchId);
  const updated: Student = {
    ...mockStudents[index],
    firstName: data.firstName,
    lastName: data.lastName,
    fullName: `${data.firstName} ${data.lastName}`.trim(),
    email: data.email,
    phone: data.phone,
    gender: data.gender,
    dateOfBirth: data.dateOfBirth,
    guardianName: data.guardianName,
    guardianPhone: data.guardianPhone,
    address: data.address,
    batchId: data.batchId,
    batchName: batch?.name ?? mockStudents[index].batchName,
    school: data.school,
    status: data.status,
  };

  mockStudents[index] = updated;
  return updated;
}

export function deleteStudent(id: string): boolean {
  const index = mockStudents.findIndex((student) => student.id === id);
  if (index === -1) return false;

  mockStudents.splice(index, 1);
  return true;
}